    private void txtCodKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String cod = txtCod.getText();
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3307/vendas?user=root&password=&useUnicode=true&characterEncoding=UTF-8");
                pst = con.prepareStatement("select * from produto where id = ?");
                
                pst.setString(1, cod);
                rs = pst.executeQuery();
                
                if(rs.next() == false){
                    JOptionPane.showMessageDialog(this, "Cod do produto n√£o encontrado");
                }else{
                    String nomeProd = rs.getString("nomeproduto");
                    String preco = rs.getString("preco");
                    
                    txtNomeProd.setText(nomeProd.trim());
                    txtPreco.setText(preco.trim());
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txtCodKeyPressed

    private void txtQuantidadeStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_txtQuantidadeStateChanged
        int qtd = Integer.parseInt(txtQuantidade.getValue().toString());
        int preco = Integer.parseInt(txtPreco.getText());
        
        int total = qtd * preco;
        
        txtValorTotal.setText(String.valueOf(total));
    }//GEN-LAST:event_txtQuantidadeStateChanged
    
    public void troco(){
        int total = Integer.parseInt(txtTotal.getText());
        int pagamento = Integer.parseInt(txtPag.getText());
        
        int troco = pagamento - total;
        txtTroco.setText(String.valueOf(troco));
    }
    
    public void pagar(){
        String total = txtTotal.getText();
        String pagamento = txtPag.getText();
        String troco = txtTroco.getText();
        
        DefaultTableModel model = new DefaultTableModel();
        
        model = (DefaultTableModel)jTable1.getModel();
        
        txtConta.setText(txtConta.getText() + "------------CONTA--------------\n");
        
        
        txtConta.setText(txtConta.getText() + "Produto" + "\t" + "Pre√ßo" + "\t" + "Total" + "\n");
        
        for(int i = 0 ; i < model.getRowCount() ; i ++){
            String nomeProd = (String)model.getValueAt(i, 1);
            String preco = (String)model.getValueAt(i, 3);
            String tot = (String)model.getValueAt(i, 4);
            
            txtConta.setText(txtConta.getText() + nomeProd + "\t" + preco + "\t" + tot + "\n");
        }
        
        txtConta.setText(txtConta.getText() + "\n");
        txtConta.setText(txtConta.getText() + "\n");
        txtConta.setText(txtConta.getText() + "\t" + "\t" + "Valor Total: " + total + "\n");
        txtConta.setText(txtConta.getText() + "\t" + "\t" + "Valor Pago: " + pagamento + "\n");
        txtConta.setText(txtConta.getText() + "\t" + "\t" + "Troco: " + troco + "\n");
        txtConta.setText(txtConta.getText() + "\n");
        txtConta.setText(txtConta.getText() + "-------------------------------");
        
        
    }
    
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = new DefaultTableModel();
        
        model = (DefaultTableModel)jTable1.getModel();
        
        model.addRow(new Object[]{
            txtCod.getText(),
            txtNomeProd.getText(),
            txtQuantidade.getValue().toString(),
            txtPreco.getText(),
            txtValorTotal.getText(),
        });
        
        int sum = 0;
        
        for(int i = 0 ; i < jTable1.getRowCount() ; i++){
            sum = sum + Integer.parseInt(jTable1.getValueAt(i, 4).toString());
        }
        
        txtTotal.setText(Integer.toString(sum));
        
        txtCod.setText("");
        txtNomeProd.setText("");
        txtPreco.setText("");
        txtValorTotal.setText("");
        txtCod.requestFocus();
        
    }//GEN-LAST:event_jButton1ActionPerformed

